        <br> 　　（一）出口货物为生产企业的自产货物或视同自产货物。
        <br> 　　（二）生产企业为增值税一般纳税人并已按规定办理出口退（免）税备案。
        <br> 　　（三）生产企业已与境外单位或个人签订出口合同。
        <br> 　　（四）生产企业已与综服企业签订外贸综合服务合同（协议），约定由综服企业提供包括报关报检、物流、代办退税、结算等在内的综合服务，并明确相关法律责任。
        <br> 　　（五）生产企业向主管税务机关提供代办退税的开户银行和账号（以下简称代办退税账户）。
        <br> 　　三、生产企业应当办理委托代办退税备案。生产企业在已办理出口退（免）税备案后，首次委托综服企业代办退税前，向其所在地主管税务机关报送《代办退税情况备案表》（附件1）并提供代办退税账户，同时将与综服企业签订的外贸综合服务合同（协议）留存备查。
        <br> 　　《代办退税情况备案表》内容发生变化时，生产企业应自发生变化之日起30日内重新报送该表。
        <br> 　　生产企业办理撤回委托代办退税备案事项的,应在综服企业主管税务机关按规定向综服企业结清该生产企业的代办退税款后办理。
        <br> 　　生产企业办理撤回出口退（免）税备案事项的，应按规定先办理撤回委托代办退税备案事项。
        <br> 　　四、综服企业应当办理代办退税备案。综服企业办理出口退（免）税备案后，在为每户生产企业首次代办退税前，向其所在地主管税务机关报送《代办退税情况备案表》，同时将下列资料留存备查：
        <br> 　　（一）与生产企业签订的外贸综合服务合同（协议）。
        <br> 　　（二）每户委托代办退税生产企业的《代办退税情况备案表》。
        <br> 　　（三）综服企业代办退税内部风险管控信息系统建设及应用情况。
        <br> 　　《代办退税情况备案表》的内容发生变化时，综服企业应自发生变化之日起30日内重新报送该表。
        <br> 　　综服企业首次办理代办退税备案时，应将企业代办退税内部风险管控制度一次性报主管税务机关。
        <br> 　　五、综服企业主管税务机关应将综服企业报送的《代办退税情况备案表》内容与相应生产企业的《代办退税情况备案表》内容进行比对，比对相符的，应予以办理代办退税备案；比对不符的，将比对不符情况一次性告知综服企业。
        <br> 　　六、生产企业代办退税的出口货物，应先按出口货物离岸价和增值税适用税率计算销项税额并按规定申报缴纳增值税，同时向综服企业开具备注栏内注明“代办退税专用”的增值税专用发票（以下称代办退税专用发票），作为综服企业代办退税的凭证。
        <br> 　　出口货物离岸价以人民币以外的货币结算的，其人民币折合率可以选择销售额发生的当天或者当月1日的人民币汇率中间价。
        <br> 　　代办退税专用发票上的“金额”栏次须按照换算成人民币金额的出口货物离岸价填写。
        <br> 　　2017年11月1日后报关出口的货物，如生产企业在2017年11月1日前已向综服企业开具增值税专用发票（除代办退税专用发票外）的，仍按照国家税务总局公告2014年第13号的规定办理出口退税。
