        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;为促进外贸综合服务企业规范健康发展，建立与企业发展相适应的出口退（免）税管理模式，根据《商务部海关总署税务总局质检总局外汇局关于促进外贸综合服务企业健康发展有关工作的通知》（商贸函〔2017〕759号）的精神，现将外贸综合服务企业代生产企业办理出口退（免）税事项的有关问题公告如下：<br> 　　一、外贸综合服务企业（以下简称综服企业）代国内生产企业办理出口退（免）税事项同时符合下列条件的，可由综服企业向综服企业所在地主管税务机关集中代为办理出口退（免）税事项（以下称代办退税）:
        <br> 　　（一）符合商务部等部门规定的综服企业定义并向主管税务机关备案。
        <br> 　　（二）企业内部已建立较为完善的代办退税内部风险管控制度并已向主管税务机关备案。
